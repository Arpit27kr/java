
public class methodparameter { 
	
	static void name(String fname, int age)   //information passed to method as variable
	{
		
		
		System.out.println(fname+ "My age is " +age );
		
	}
	
	public static void main(String[] args)
	{
		name("jacob ", 8);           // it will print jacob
		name("lily ",16);
		
	}

}
